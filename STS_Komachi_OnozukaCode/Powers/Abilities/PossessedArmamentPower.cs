namespace STS_Komachi_Onozuka.STS_Komachi_OnozukaCode.Powers.Abilities
{ 
    public class PossessedArmamentPower : STS_Komachi_OnozukaPower
    {
        public override PowerType Type => PowerType.Buff;
        public override PowerStackType StackType => PowerStackType.Counter;

        protected override IEnumerable<IHoverTip> ExtraHoverTips => [HoverTipFactory.FromPower<VengefulSpiritPower>()];

        public override async Task AfterDamageGiven(PlayerChoiceContext choiceContext, Creature? dealer, DamageResult result, ValueProp props, Creature target, CardModel? cardSource)
        {
            if (dealer == base.Owner && props.IsPoweredAttack() && result.UnblockedDamage > 0)
            {
                await PowerCmd.Apply<VengefulSpiritPower>(choiceContext, target, base.Amount, base.Owner, null);
            }
        }
    }
