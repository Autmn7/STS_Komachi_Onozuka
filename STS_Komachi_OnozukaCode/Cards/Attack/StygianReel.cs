namespace STS_Komachi_Onozuka.STS_Komachi_OnozukaCode.Cards.Attack
{
      
    public class StygianReel : STS_Komachi_OnozukaCard
    {
        public StygianReel()
        : base(2, CardType.Attack, CardRarity.Uncommon, TargetType.AnyEnemy)
        {
            WithDamage(12);
            WithPower<DistancePower>(nameof(Value1), 1, 1);
            WithKeyword(KomachiKeywords.Displace);
        }

        protected override async Task OnPlay(PlayerChoiceContext choiceContext, CardPlay cardPlay)
        {
            ArgumentNullException.ThrowIfNull(cardPlay.Target, "cardPlay.Target");
            await DamageCmd.Attack(DynamicVars.Damage.BaseValue).FromCard(this).Targeting(cardPlay.Target)
                .WithHitFx("vfx/vfx_attack_slash")
                .Execute(choiceContext);

            await DistanceCmd.Displace(choiceContext, cardPlay.Target, -Value1, Owner.Creature);

            await PowerCmd.Apply<DisplaceNextTurnPower>(choiceContext, cardPlay.Target, Value1, Owner.Creature, this);
        }
    }
}
